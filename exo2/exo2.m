Whoami (who am i) est une commande UNIX permettant d'afficher le nom de l'usager associé à l'identificateur effectif (user ID ou UID) courant de 
l'usager.

Pwd est une commande UNIX. Elle permet d'afficher le chemin d'accès vers le répertoire où se situe l'utilisateur qui a entré la commande. 

Chown est un appel système et une commande UNIX nécessitant les droits de l'utilisateur racine (root) pour changer le propriétaire d'un 
fichier ou d'un dossier.

Clear est une commande UNIX permettant de replacer la ligne de commande en haut de l'écran.


